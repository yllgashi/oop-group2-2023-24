package lecture20_ref_primitive_types_1;

public class Main {
    public static void main(String[] args) {
        // primitive type
        // (we do not have any methods or attributes of this)
        // are stored in stack memory
        int primitiveType = 10;

        // reference type
        // (we have any methods or attributes of this)
        // are stored in heap memory
        String referenceType = "10";
    }
}
